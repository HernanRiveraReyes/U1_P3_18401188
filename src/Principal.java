    public void fig(){
        int in = jComboBox1.getSelectedIndex();
        switch(in){
            case 0: 
                JOptionPane.showMessageDialog(null, "Selecciona Una Opcion Que Mostrar!");
                
                break;
            case 1:
                
                if(jRadioButton1.isSelected()){
                    jLabel1.setIcon(new ImageIcon( getClass().getResource("/catSad64.png")));
                }else{
                    jLabel1.setIcon(new ImageIcon( getClass().getResource("/CatB64.png")));
                }
                break;
            case 2:
                if(jRadioButton1.isSelected()){
                    jLabel1.setIcon(new ImageIcon( getClass().getResource("/b64.png")));
                }else{
                    jLabel1.setIcon(new ImageIcon( getClass().getResource("/pmario64.png")));
                }
                break;
        }
    }
